package it.uniroma3.tech4ch.charts;

public class ChartDataPoint3D{
	private String Z;

	private String X;
	private Integer Y;
	
	public String getX() {
		return X;
	}
	public void setX(String x) {
		X = x;
	}
	public Integer getY() {
		return Y;
	}
	public void setY(Integer y) {
		Y = y;
	}
	public String getZ() {
		return Z;
	}

	public void setZ(String z) {
		Z = z;
	}

}
